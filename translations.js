// Translation function
function translatePage(lang = 'en') {
    // Store current language in localStorage
    localStorage.setItem('selectedLanguage', lang);
    
    // Update all elements with data-translate attribute (for text content)
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
    
    // Update all elements with data-translate-placeholder attribute (for placeholders)
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (translations[lang] && translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });
    
    // Update HTML lang attribute
    document.documentElement.lang = lang;
    
    // Update language selector text
    const languageSelectorText = document.querySelector('.language-selector-text');
    if (languageSelectorText) {
        languageSelectorText.textContent = languageNames[lang] || 'English';
    }
}

// Language names mapping
const languageNames = {
    en: 'English',
    sv: 'Swedish',
    tr: 'Turkish',
    no: 'Norwegian',
    da: 'Danish',
    de: 'German'
};

// Initialize translation on page load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('selectedLanguage') || 'en';
    translatePage(savedLang);
    
    // Set up custom language selector
    const languageSelectorWrapper = document.querySelector('.language-selector-wrapper');
    const languageSelectorButton = document.getElementById('language-selector-button');
    const languageSelectorText = languageSelectorButton?.querySelector('.language-selector-text');
    const languageSelectorDropdown = document.getElementById('language-selector-dropdown');
    const languageOptions = document.querySelectorAll('.language-option');
    
    if (languageSelectorWrapper && languageSelectorButton && languageSelectorText && languageSelectorDropdown) {
        // Set initial selected language text
        if (languageSelectorText) {
            languageSelectorText.textContent = languageNames[savedLang] || 'English';
        }
        
        // Update selected language indicator
        const updateSelectedLanguage = (selectedLang) => {
            languageOptions.forEach(option => {
                const lang = option.getAttribute('data-lang');
                if (lang === selectedLang) {
                    option.setAttribute('data-lang-selected', 'true');
                } else {
                    option.removeAttribute('data-lang-selected');
                }
            });
        };
        
        updateSelectedLanguage(savedLang);
        
        // Toggle language list on button click
        languageSelectorButton.addEventListener('click', (e) => {
            e.stopPropagation();
            languageSelectorWrapper.classList.toggle('active');
        });
        
        // Close language list when clicking outside
        document.addEventListener('click', (e) => {
            if (!languageSelectorWrapper.contains(e.target)) {
                languageSelectorWrapper.classList.remove('active');
            }
        });
        
        // Handle language option clicks
        languageOptions.forEach((option, index) => {
            option.addEventListener('click', (e) => {
                e.stopPropagation();
                const lang = option.getAttribute('data-lang');
                
                // Animate all options out to the left before closing
                languageOptions.forEach(opt => {
                    opt.style.transition = 'opacity 0.2s ease, transform 0.2s ease';
                    opt.style.opacity = '0';
                    opt.style.transform = 'translateX(-20px)';
                });
                
                setTimeout(() => {
                    translatePage(lang);
                    updateSelectedLanguage(lang);
                    languageSelectorWrapper.classList.remove('active');
                    
                    // Reset all option styles after a brief delay
                    setTimeout(() => {
                        languageOptions.forEach(opt => {
                            opt.style.transition = '';
                            opt.style.opacity = '';
                            opt.style.transform = '';
                        });
                    }, 100);
                }, 200);
            });
        });
    }
});

